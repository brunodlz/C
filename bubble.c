#include <stdio.h>

void banner();

int main()
{
 int numbers[10],swap;
 int i,j;

 banner();

 for(i=0;i<10;i++)
 {
  printf("%d number: ",i+1); scanf("%d",&numbers[i]);
 }

 for(i=1;i<10;i++)
 {
  for(j=0;j<10-1;j++)
  {
   if(numbers[j]>numbers[j+1])
   {
    swap = numbers[j];
    numbers[j] = numbers[j+1];
    numbers[j+1] = swap;
   }
  }
 }

 printf("=============== new list ===============\n");

 for(i=0;i<10;i++)
 {
  printf("number: %d\n",numbers[i]);
 }

 return 0;
}

